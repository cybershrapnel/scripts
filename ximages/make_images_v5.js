function randomize(arr) {
    return arr[Math.floor(Math.random() * arr.length)];
}

// Combine elements to create prompts
function generatePrompt() {
    return `${randomize(basePrompts)}, ${randomize(settings)}, ${randomize(styles)}, ${randomize(extraDetails)}`;
}

// Function to automate interactions
(function automateInteraction() {
    function setNativeValue(element, value) {
        const valueSetter = Object.getOwnPropertyDescriptor(Object.getPrototypeOf(element), 'value').set;
        valueSetter.call(element, value);
        element.dispatchEvent(new Event('input', { bubbles: true }));
    }

    function waitForButtonAndClick() {
        const button = document.querySelector('button[aria-label="Grok something"]');
        if (button && !button.disabled) {
            button.click();
            console.log('Button clicked!');
            setTimeout(processImage, 30000); // Wait 30 seconds before processing the image
        } else {
            setTimeout(waitForButtonAndClick, 500);
        }
    }

    function processImage() {
        const images = document.querySelectorAll('img');
        const targetImage = images[images.length - 1];
        if (targetImage && targetImage.complete) {
            const canvas = document.createElement('canvas');
            canvas.width = targetImage.naturalWidth;
            canvas.height = targetImage.naturalHeight;
            const ctx = canvas.getContext('2d');
            ctx.drawImage(targetImage, 0, 0);
            canvas.toBlob((blob) => {
                const a = document.createElement('a');
                a.href = URL.createObjectURL(blob);
                a.download = `NanoCheeZe_${Date.now()}.png`;
                a.click();
                URL.revokeObjectURL(a.href);
            });
        }
        setTimeout(() => startAutomation(), 5000);
    }

    function startAutomation() {
        const textarea = document.querySelector('textarea[placeholder="Ask anything"]');
        if (textarea) {
            const prompt = generatePrompt();
            setNativeValue(textarea, prompt);
            console.log(`Generated prompt: ${prompt}`);
            waitForButtonAndClick();
        } else {
            setTimeout(startAutomation, 500);
        }
    }

    startAutomation();
})();
